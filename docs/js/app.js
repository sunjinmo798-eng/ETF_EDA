/**
 * Naver ETF Interactive EDA Dashboard - Core Logic (JS)
 */

let etfData = [];
let filteredData = [];
let currentPage = 1;
const rowsPerPage = 20;
let sortColumn = 'marketSum';
let sortDirection = 'desc';
let currentTag = 'all';

// DOM Elements
const elementCache = {
    updatedAt: document.getElementById('updatedAt'),
    kpiTotalCount: document.getElementById('kpiTotalCount'),
    kpiTotalMarketSum: document.getElementById('kpiTotalMarketSum'),
    kpiUpCount: document.getElementById('kpiUpCount'),
    kpiDownCount: document.getElementById('kpiDownCount'),
    tableBody: document.getElementById('tableBody'),
    searchInput: document.getElementById('searchInput'),
    refreshBtn: document.getElementById('refreshBtn'),
    pageInfo: document.getElementById('pageInfo'),
    btnPrevPage: document.getElementById('btnPrevPage'),
    btnNextPage: document.getElementById('btnNextPage')
};

// Initialize Application
document.addEventListener('DOMContentLoaded', () => {
    initEvents();
    loadDashboardData();
});

function initEvents() {
    // Search input event
    elementCache.searchInput.addEventListener('input', (e) => {
        applyFilters(e.target.value);
    });

    // Refresh button event
    elementCache.refreshBtn.addEventListener('click', () => {
        loadDashboardData(true);
    });

    // Pagination buttons
    elementCache.btnPrevPage.addEventListener('click', () => {
        if (currentPage > 1) {
            currentPage--;
            renderTable();
        }
    });

    elementCache.btnNextPage.addEventListener('click', () => {
        const maxPage = Math.ceil(filteredData.length / rowsPerPage);
        if (currentPage < maxPage) {
            currentPage++;
            renderTable();
        }
    });

    // Category tag filter buttons
    document.querySelectorAll('.tag-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            document.querySelectorAll('.tag-btn').forEach(b => b.classList.remove('active'));
            e.target.classList.add('active');
            currentTag = e.target.dataset.tag;
            applyFilters(elementCache.searchInput.value);
        });
    });

    // Table header sort
    document.querySelectorAll('table.data-table th[data-sort]').forEach(th => {
        th.addEventListener('click', () => {
            const col = th.dataset.sort;
            if (sortColumn === col) {
                sortDirection = sortDirection === 'asc' ? 'desc' : 'asc';
            } else {
                sortColumn = col;
                sortDirection = 'desc';
            }
            sortData();
            renderTable();
        });
    });
}

// Data Fetching
async function loadDashboardData(isRefresh = false) {
    if (isRefresh) {
        elementCache.refreshBtn.classList.add('spinning');
    }

    try {
        // 1. Fetch static JSON data generated by python script
        const response = await fetch('./data/etf_latest.json?t=' + Date.now());
        if (!response.ok) throw new Error('Data load failed');
        const json = await response.json();

        etfData = json.items || [];
        elementCache.updatedAt.textContent = json.updated_at || new Date().toLocaleString();

        // Process data & Render
        updateKpiCards();
        applyFilters(elementCache.searchInput.value);
        renderCharts();
    } catch (err) {
        console.error('Failed to load ETF data:', err);
        alert('데이터를 불러오는데 실패했습니다.');
    } finally {
        if (isRefresh) {
            elementCache.refreshBtn.classList.remove('spinning');
        }
    }
}

// KPI Summary calculation
function updateKpiCards() {
    if (!etfData.length) return;

    const totalCount = etfData.length;
    const totalMarketSum = etfData.reduce((acc, curr) => acc + (curr.marketSum || 0), 0);
    const upCount = etfData.filter(item => (item.changeRate || 0) > 0).length;
    const downCount = etfData.filter(item => (item.changeRate || 0) < 0).length;

    elementCache.kpiTotalCount.textContent = totalCount.toLocaleString() + ' 개';
    
    // Format Market Sum (억 -> 조/억)
    const marketSumTrillion = (totalMarketSum / 10000).toFixed(1);
    elementCache.kpiTotalMarketSum.textContent = `${marketSumTrillion} 조원`;

    elementCache.kpiUpCount.textContent = `${upCount} 종목`;
    elementCache.kpiDownCount.textContent = `${downCount} 종목`;
}

// Filtering Logic
function applyFilters(searchKeyword = '') {
    const kw = searchKeyword.trim().toLowerCase();

    filteredData = etfData.filter(item => {
        const matchesKw = !kw || 
            item.itemname.toLowerCase().includes(kw) || 
            item.itemcode.toLowerCase().includes(kw);

        if (!matchesKw) return false;

        if (currentTag === 'all') return true;
        if (currentTag === 'leverage') return item.itemname.includes('레버리지') || item.itemname.includes('2X');
        if (currentTag === 'inverse') return item.itemname.includes('인버스') || item.itemname.includes('곱버스');
        if (currentTag === 'bond') return item.itemname.includes('채권') || item.itemname.includes('KOFR') || item.itemname.includes('CD금리');
        if (currentTag === 'tech') return item.itemname.includes('반도체') || item.itemname.includes('IT') || item.itemname.includes('AI') || item.itemname.includes('빅테크');
        if (currentTag === 'battery') return item.itemname.includes('2차전지') || item.itemname.includes('배터리');
        if (currentTag === 'us') return item.itemname.includes('미국') || item.itemname.includes('S&P500') || item.itemname.includes('나스닥');

        return true;
    });

    sortData();
    currentPage = 1;
    renderTable();
}

// Sorting Logic
function sortData() {
    filteredData.sort((a, b) => {
        let valA = a[sortColumn];
        let valB = b[sortColumn];

        if (valA === undefined || valA === null) valA = -999999;
        if (valB === undefined || valB === null) valB = -999999;

        if (typeof valA === 'string') {
            return sortDirection === 'asc' 
                ? valA.localeCompare(valB) 
                : valB.localeCompare(valA);
        }

        return sortDirection === 'asc' ? valA - valB : valB - valA;
    });
}

// Table Rendering & Pagination
function renderTable() {
    const totalItems = filteredData.length;
    const maxPage = Math.ceil(totalItems / rowsPerPage) || 1;

    if (currentPage > maxPage) currentPage = maxPage;

    const startIdx = (currentPage - 1) * rowsPerPage;
    const pageItems = filteredData.slice(startIdx, startIdx + rowsPerPage);

    let html = '';
    pageItems.forEach(item => {
        const rate = item.changeRate || 0;
        const rateClass = rate > 0 ? 'text-up' : (rate < 0 ? 'text-down' : 'text-flat');
        const rateSign = rate > 0 ? '+' : '';

        const m3Rate = item.threeMonthEarnRate || 0;
        const m3Class = m3Rate > 0 ? 'text-up' : (m3Rate < 0 ? 'text-down' : 'text-flat');

        html += `
            <tr>
                <td><span class="badge-code">${item.itemcode}</span></td>
                <td style="font-weight:600;">${item.itemname}</td>
                <td style="font-weight:700;">${(item.nowVal || 0).toLocaleString()} 원</td>
                <td class="${rateClass}">${rateSign}${rate.toFixed(2)}%</td>
                <td>${(item.marketSum || 0).toLocaleString()} 억</td>
                <td>${(item.quant || 0).toLocaleString()}</td>
                <td>${(item.amonut || 0).toLocaleString()} 백만</td>
                <td class="${m3Class}">${m3Rate ? m3Rate.toFixed(2) + '%' : '-'}</td>
            </tr>
        `;
    });

    elementCache.tableBody.innerHTML = html || '<tr><td colspan="8" style="text-align:center; padding: 30px;">검색 결과가 없습니다.</td></tr>';
    elementCache.pageInfo.textContent = `${currentPage} / ${maxPage} 페이지 (총 ${totalItems}개)`;
    
    elementCache.btnPrevPage.disabled = currentPage === 1;
    elementCache.btnNextPage.disabled = currentPage === maxPage;
}

// Plotly Charts Rendering
function renderCharts() {
    if (!etfData.length) return;

    const themeLayout = {
        paper_bgcolor: 'rgba(0,0,0,0)',
        plot_bgcolor: 'rgba(0,0,0,0)',
        font: { color: '#94a3b8', family: 'Plus Jakarta Sans' },
        margin: { t: 30, r: 20, l: 140, b: 40 },
        xaxis: { gridcolor: 'rgba(255,255,255,0.06)', zerolinecolor: 'rgba(255,255,255,0.1)' },
        yaxis: { gridcolor: 'rgba(255,255,255,0.06)', zerolinecolor: 'rgba(255,255,255,0.1)' }
    };

    // 1. 시가총액 Top 15 Chart
    const topMarketCap = [...etfData]
        .sort((a, b) => (b.marketSum || 0) - (a.marketSum || 0))
        .slice(0, 12)
        .reverse();

    const trace1 = {
        x: topMarketCap.map(d => d.marketSum),
        y: topMarketCap.map(d => d.itemname.length > 15 ? d.itemname.slice(0, 15) + '...' : d.itemname),
        type: 'bar',
        orientation: 'h',
        marker: {
            color: topMarketCap.map((_, i) => `rgba(99, 102, 241, ${0.4 + (i / 12) * 0.6})`),
            line: { color: 'rgba(99, 102, 241, 0.8)', width: 1 }
        },
        hovertemplate: '%{y}<br>시가총액: %{x:,.0f} 억 원<extra></extra>'
    };

    Plotly.newPlot('chartMarketCap', [trace1], {
        ...themeLayout,
        title: { text: '시가총액 Top 12 ETF (억 원)', font: { color: '#f8fafc', size: 14 } }
    }, { responsive: true, displayModeBar: false });

    // 2. 3개월 수익률 Top 10 Chart
    const topPerformers = [...etfData]
        .filter(d => d.threeMonthEarnRate !== undefined && d.threeMonthEarnRate !== null)
        .sort((a, b) => b.threeMonthEarnRate - a.threeMonthEarnRate)
        .slice(0, 10)
        .reverse();

    const trace2 = {
        x: topPerformers.map(d => d.threeMonthEarnRate),
        y: topPerformers.map(d => d.itemname.length > 15 ? d.itemname.slice(0, 15) + '...' : d.itemname),
        type: 'bar',
        orientation: 'h',
        marker: {
            color: topPerformers.map(d => d.threeMonthEarnRate >= 0 ? '#ef4444' : '#3b82f6')
        },
        hovertemplate: '%{y}<br>3개월 수익률: %{x:.2f}%<extra></extra>'
    };

    Plotly.newPlot('chartPerformance', [trace2], {
        ...themeLayout,
        title: { text: '3개월 수익률 Top 10 ETF (%)', font: { color: '#f8fafc', size: 14 } }
    }, { responsive: true, displayModeBar: false });

    // 3. 거래대금 vs 등락률 Scatter Plot
    const scatterData = etfData.slice(0, 100); // 상위 100개
    const trace3 = {
        x: scatterData.map(d => d.amonut || 0),
        y: scatterData.map(d => d.changeRate || 0),
        mode: 'markers',
        text: scatterData.map(d => d.itemname),
        marker: {
            size: scatterData.map(d => Math.max(8, Math.min(40, Math.sqrt(d.marketSum || 0) / 4))),
            color: scatterData.map(d => d.changeRate >= 0 ? '#ef4444' : '#3b82f6'),
            opacity: 0.7,
            line: { width: 1, color: '#ffffff' }
        },
        hovertemplate: '<b>%{text}</b><br>거래대금: %{x:,.0f} 백만 원<br>등락률: %{y:.2f}%<extra></extra>'
    };

    Plotly.newPlot('chartScatter', [trace3], {
        ...themeLayout,
        margin: { t: 30, r: 30, l: 50, b: 50 },
        title: { text: '시가총액 상위 100개 ETF [거래대금 vs 등락률] (버블크기: 시가총액)', font: { color: '#f8fafc', size: 14 } },
        xaxis: { title: '거래대금 (백만 원)', gridcolor: 'rgba(255,255,255,0.06)' },
        yaxis: { title: '등락률 (%)', gridcolor: 'rgba(255,255,255,0.06)' }
    }, { responsive: true, displayModeBar: false });
}
